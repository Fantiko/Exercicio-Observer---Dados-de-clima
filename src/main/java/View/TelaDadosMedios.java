    @Override
    public void atualizar(List<DadoClima> TodosDados) {
        if(TodosDados.isEmpty()){
            infoPressao.setText(String.valueOf(0));
            infoRegistros.setText(String.valueOf(0));
            infoTemperatura.setText(String.valueOf(0));
            infoUmidade.setText(String.valueOf(0));

            return;
        }



        float somaTemperatura = 0;
        float somaUmidade = 0;
        float somaPressao = 0;

        for (DadoClima dado : TodosDados) {
            somaTemperatura += dado.getTemperatura();
            somaUmidade += dado.getUmidade();
            somaPressao += dado.getPressao();
        }

        int nRegistros = TodosDados.size();


        infoPressao.setText(String.valueOf(somaPressao/nRegistros));
        infoRegistros.setText(String.valueOf(nRegistros));
        infoTemperatura.setText(String.valueOf(somaTemperatura/nRegistros));
        infoUmidade.setText(String.valueOf(somaUmidade/nRegistros));
    }

